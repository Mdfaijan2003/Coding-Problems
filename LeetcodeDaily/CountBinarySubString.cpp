/*696. Count Binary Substrings

Given a binary string s, return the number of non-empty substrings that have the same number of 0's and 1's, and all the 0's and all the 1's in these substrings are grouped consecutively.

Substrings that occur multiple times are counted the number of times they occur.



Example 1:

Input: s = "00110011"
Output: 6
Explanation: There are 6 substrings that have equal number of consecutive 1's and 0's: "0011", "01", "1100", "10", "0011", and "01".
Notice that some of these substrings repeat and are counted the number of times they occur.
Also, "00110011" is not a valid substring because all the 0's (and 1's) are not grouped together.*/

#include <bits/stdc++.h>
using namespace std;
// Approach --> Track the consecutive counts of 0s and 1s encountered so far. For each new segment, compare the current count with the previous one; the result is determined by the smaller value.
int countBinarySubstrings(string s)
{
  int n = s.size();
  int prev = 0;
  int curr = 1;
  int result = 0;
  for (int i = 1; i < n; i++)
  {
    if (s[i] == s[i - 1])
    {
      curr++;
    }
    else
    {
      result += min(prev, curr);
      prev = curr;
      curr = 1;
    }
  }

  return result + min(prev, curr);
}
int main()
{
  string s ="00110011";
  cout<<countBinarySubstrings(s);

  return 0;
}